def draw_char(graphics, ch, ox, oy, pen, scale=2):
    """Draw a 5x7 font character scaled by `scale` using PicoGraphics."""
    bitmap = FONT.get(ch)
    if not bitmap:
        return
    graphics.set_pen(pen)
    for row in range(FONT_H):
        for col in range(FONT_W):
            if bitmap[row] & (1 << (FONT_W - 1 - col)):
                for sy in range(scale):
                    for sx in range(scale):
                        graphics.pixel(ox + col * scale + sx, oy + row * scale + sy)


def draw_string(graphics, text, ox, oy, pen, scale=2):
    """Draw a string using the 5x7 font at given scale."""
    gap = 2 if scale > 1 else 1
    spacing = FONT_W * scale + gap
    for i, ch in enumerate(text):
        draw_char(graphics, ch, ox + i * spacing, oy, pen, scale)


def string_width(text, scale=2):
    """Calculate pixel width of a string."""
    gap = 2 if scale > 1 else 1
    spacing = FONT_W * scale + gap
    return len(text) * spacing - gap if text else 0


def draw_tiny(graphics, ch, tx, ty, pen):
    """Draw a 3x5 tiny font character."""
    glyph = TINY.get(ch)
    if not glyph:
        return
    graphics.set_pen(pen)
    for ri, row in enumerate(glyph):
        for ci, v in enumerate(row):
            if v:
                graphics.pixel(tx + ci, ty + ri)


def draw_tiny_str(graphics, text, tx, ty, pen):
    """Draw a string using the 3x5 tiny font."""
    for i, ch in enumerate(text):
        draw_tiny(graphics, ch, tx + i * 4, ty, pen)


def tiny_str_width(text):
    """Calculate pixel width of a tiny font string."""
    return len(text) * 4 - 1 if text else 0


def draw_tiny_centered(graphics, text, ty, pen, cols=128):
    """Draw a tiny font string horizontally centered."""
    w = tiny_str_width(text)
    tx = (cols - w) // 2
    draw_tiny_str(graphics, text, tx, ty, pen)
